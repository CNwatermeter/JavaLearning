public class literal {
    public static void main(String[] args) {
        //integers
        System.out.println(100);
        System.out.println(-100);
        //floating-point numbers
        System.out.println(3.14);
        //characters
        System.out.println('@');
        //special characters
        //tab character: used to create a tab space between text
        System.out.println("name"+'\t'+"age");
        System.out.println("tom"+'\t'+"24");
        //strings
        System.out.println("爪哇");
        //boolean values only includes true and false two values
        System.out.println(true);
        System.out.println(false);
        //and null values, it can not be printed
    }
}